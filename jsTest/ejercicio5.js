var array = new Array();
array = [1,2,3,4];

var length = array.length;
for(var i = 0; i < length; i++)
{
    switch(i)
    {
        case 0:
            console.log("UNO")
            break;

        case 1:
             console.log("DOS")
            break;

        case 2:
            console.log("TRES")
            break;

        case 3:
            console.log("CUATRO")
            break;     
    }
}

